package bean;

public class WX {

	String yy;
	String xm;
	String dh;
	String ld;
	public WX(String yy, String xm, String dh, String ld) {
		super();
		this.yy = yy;
		this.xm = xm;
		this.dh = dh;
		this.ld = ld;
	}
	public String getYy() {
		return yy;
	}
	public void setYy(String yy) {
		this.yy = yy;
	}
	public String getXm() {
		return xm;
	}
	public void setXm(String xm) {
		this.xm = xm;
	}
	public String getDh() {
		return dh;
	}
	public void setDh(String dh) {
		this.dh = dh;
	}
	public String getLd() {
		return ld;
	}
	public void setLd(String ld) {
		this.ld = ld;
	}
	
	
}
